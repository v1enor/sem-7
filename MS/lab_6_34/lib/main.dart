class MyHomePage extends StatefulWidget {
  const MyHomePage({super.key, required this.title});

  // This widget is the home page of your application. It is stateful, meaning
  // that it has a State object (defined below) that contains fields that affect
  // how it looks.

  // This class is the configuration for the state. It holds the values (in this
  // case the title) provided by the parent (in this case the App widget) and
  // used by the build method of the State. Fields in a Widget subclass are
  // always marked "final".

  final String title;

  @override
  State<MyHomePage> createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  static const platform = MethodChannel('samples.flutter.dev/battery');
  var _platform_data = MethodChannel('samples.flutter.dev/data');

  // Get battery level.
  String _batteryLevel = 'Unknown battery level.';

  Future<void> _getBatteryLevel() async {
    String batteryLevel;
    try {
      final int result = await platform.invokeMethod('getBatteryLevel');
      batteryLevel = 'Battery level at $result % .';
    } on PlatformException catch (e) {
      batteryLevel = "Failed to get battery level: '${e.message}'.";
    }

    setState(() {
      _batteryLevel = batteryLevel;
    });
  }

  void _getDataFromPlatform() async {
    try {
      final String data = await _platform_data.invokeMethod('getData');
      print('что мы открыли: $data');
    } catch (e) {
      print('Error getting data from platform: $e');
    }
  }

  void _setDataToPlatform() async {
    try {
      final String data = 'ilya';
      await _platform_data.invokeMethod('setData', data);
      print('а кто тут такой? -  $data');
    } catch (e) {
      print('Error sending data to platform: $e');
    }
  }

  @override
  Widget build(BuildContext context) {
    return Material(
      child: Center(
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: [

            
            ElevatedButton(
              onPressed: _getBatteryLevel,
              child: const Text('Уровень батареи'),
            ),
            Text(_batteryLevel),

          ],
        ),
      ),
    );
  }
}